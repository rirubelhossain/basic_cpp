#include <stdio.h>

int main()
{
    int day ;

    scanf("%d", &day);

    switch(day)
    {
    case 1:
        printf("sunday\n");
        break ;
    case 2:
        printf("Monday\n");
        break ;
    case 3:
        printf("tuesday\n");
        break ;
    case 4:
        printf("wednesday\n");
        break ;
    case 5:
        printf("Thursday\n");
        break ;
    case 6:
        printf("Friday\n");
        break ;
    case 7:
        printf("saturday\n");
        break ;


    }


    return(0);
}
